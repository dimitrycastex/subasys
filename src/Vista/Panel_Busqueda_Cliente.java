public static ArrayList getSelectedRowsID(DefaultTableModel model){
        ArrayList lista = new ArrayList();
        for (int i = 0; i < model.getRowCount(); i++) {
            boolean sel = (Boolean)model.getValueAt(i, 0);
            if(sel){
                 
                lista.add(model.getValueAt(i, 1));
            }
            
        }
        return lista;
    }
