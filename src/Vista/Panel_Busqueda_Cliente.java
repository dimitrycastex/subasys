public static String getSelectedRowsID(DefaultTableModel model){

        for (int i = 0; i < model.getRowCount(); i++) {
            boolean sel = (Boolean)model.getValueAt(i, 0);
            if(sel){                
                return (model.getValueAt(i, 1)).toString();
            }     
        }
        return "";
    }
