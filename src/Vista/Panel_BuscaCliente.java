    private void jButton_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarActionPerformed
        // TODO add your handling code here:
        if(!this.jRadioButton_Email.isSelected() && !this.jRadioButton_NombresApellidos.isSelected()
                && !this.jRadioButton_RUT.isSelected() && !this.jRadioButton_Telefono.isSelected())
            JOptionPane.showMessageDialog(V_Principal, "Seleccione alg√∫n criterio de b√∫squeda", "Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton_BuscarActionPerformed

    private void jTextField_BuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_BuscarKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(this.jRadioButton_NombresApellidos.isSelected()){ 
            if( (!Character.isWhitespace(c) &&
                    !Character.isLetter(c) )|| this.jTextField_Buscar.getText().length()>100){
                evt.consume();
            }
        }
        
        if(this.jRadioButton_Telefono.isSelected()){
            if(!Character.isDigit(c) || this.jTextField_Buscar.getText().length()>12){
                evt.consume();
            }
        
        }
    }//GEN-LAST:event_jTextField_BuscarKeyTyped

