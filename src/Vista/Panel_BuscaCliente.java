    private void jRadioButton_RUTItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jRadioButton_RUTItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange()==ItemEvent.SELECTED){
            this.jRadioButton_NombresApellidos.setSelected(false);
            this.jRadioButton_Email.setSelected(false);
            this.jRadioButton_Telefono.setSelected(false);
        }
        
    }//GEN-LAST:event_jRadioButton_RUTItemStateChanged

